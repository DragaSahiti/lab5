/* 
   program to print all alphabets from a to z
*/

//library
  # include <iostream>
  using namespace std;

//main function
  int main() {

     //declarations
      char letter = 'a';

       //loops
       while (letter >= 'a' && letter <= 'z'){

       cout << letter << endl; 
       letter++;

        }
      


    return 0;
}
